#include "textsprites.h"

//TEXTSPRITE() CONSTRUCTOR/////////////////////////////////////////////////////
TextSprite::TextSprite(void)
{
	highlighted = false;
	Initialise();
}
///////////////////////////////////////////////////////////////////////////////

//HIGHLIGHT() FUNCTION/////////////////////////////////////////////////////////
void TextSprite::Highlight(void)
{
	bitmap = textbitmaps[1]; //set display bitmap to be highlight bitmap for text sprite
	highlighted = true;
}
///////////////////////////////////////////////////////////////////////////////

//UNHIGHLIGHT() FUNCTION/////////////////////////////////////////////////////////
void TextSprite::Unhighlight(void)
{
	bitmap = textbitmaps[0]; //set display bitmap to be normal bitmap for text sprite
	highlighted = false;
}
///////////////////////////////////////////////////////////////////////////////

//ISHIGLIGHTED() FUNCTION//////////////////////////////////////////////////////
bool TextSprite::IsHighlighted(void)
{
	return highlighted;
}
///////////////////////////////////////////////////////////////////////////////

//LOADBITMAPS(normal, highlighted) FUNCTION////////////////////////////////////
void TextSprite::LoadBitMaps(LPSTR normal, LPSTR highlighted)
{
	textbitmaps[0] = (HBITMAP)LoadImage(NULL, normal, IMAGE_BITMAP, 0, 0, LR_LOADFROMFILE);

	textbitmaps[1] = (HBITMAP)LoadImage(NULL, highlighted, IMAGE_BITMAP, 0, 0, LR_LOADFROMFILE);
}
